#[repr(u8)]
pub enum Color {
    Black  = 0x0,
    White  = 0x1,
    Green  = 0x2,
    Blue   = 0x3,
    Red    = 0x4,
    Yellow = 0x5,
    Orange = 0x6,
    Clean  = 0x7,
}

pub struct RGB(u8, u8, u8);

impl RGB {
    #[inline(always)]
    pub const fn empty() -> RGB {
        RGB(0u8, 0u8, 0u8)
    }
    #[inline(always)]
    pub const fn raw(c: u32) -> RGB {
        RGB(
            ((c >> 16) & 0xFF) as u8,
            ((c >> 8) & 0xFF) as u8,
            (c & 0xFF) as u8,
        )
    }
    #[inline(always)]
    pub const fn rgb(r: u8, b: u8, g: u8) -> RGB {
        RGB(r, g, b)
    }

    pub fn hsv(h: f32, s: f32, v: f32) -> RGB {
        let i = unsafe { floorf32(h * 6.0) };
        let (f, y) = (h * 6.0 - i, v * 255.0);
        let (p, q, t) = (
            (y * (1.0 - s)) as u8,
            (y * (1.0 - f * s)) as u8,
            (y * (1.0 - (1.0 - f) * s)) as u8,
        );
        match i as u8 % 6 {
            0 => RGB(y as u8, t, p),
            1 => RGB(q, y as u8, p),
            2 => RGB(p, y as u8, t),
            3 => RGB(p, q, y as u8),
            4 => RGB(t, p, y as u8),
            5 => RGB(y as u8, p, q),
            _ => RGB(0u8, 0u8, 0u8),
        }
    }

    #[inline(always)]
    pub fn red(&self) -> u8 {
        self.0
    }
    #[inline(always)]
    pub fn blue(&self) -> u8 {
        self.2
    }
    #[inline(always)]
    pub fn green(&self) -> u8 {
        self.1
    }
    #[inline(always)]
    pub fn uint(&self) -> u32 {
        ((self.0 as u32) << 16) | ((self.1 as u32) << 8) | self.2 as u32
    }
}

impl Eq for RGB {}
impl Copy for RGB {}
impl Clone for RGB {
    #[inline(always)]
    fn clone(&self) -> RGB {
        RGB(self.0, self.1, self.2)
    }
}
impl PartialEq for RGB {
    #[inline(always)]
    fn eq(&self, other: &RGB) -> bool {
        self.0 == other.0 && self.1 == other.1 && self.2 == other.2
    }
}
impl From<u32> for RGB {
    #[inline(always)]
    fn from(v: u32) -> RGB {
        RGB::raw(v)
    }
}
impl From<i32> for RGB {
    #[inline(always)]
    fn from(v: i32) -> RGB {
        RGB::raw(v as u32)
    }
}
impl From<(u8, u8, u8)> for RGB {
    #[inline(always)]
    fn from(v: (u8, u8, u8)) -> RGB {
        RGB(v.0, v.1, v.2)
    }
}

#[inline]
pub(super) fn dither(x: u16, y: u16, c: u32) -> u8 {
    let k = (((((c >> 16) & 0xFF) & 0xE0) << 1) | ((((c >> 8) & 0xFF) & 0xE0) >> 2) | (((c & 0xFF) & 0xE0) >> 5)) as usize;
    let i = ((x & 0x3) | ((y & 0x3) << 2)) as usize;
    MAP[k][PATTERN[i] as usize]
}
